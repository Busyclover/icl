CREATE TABLE contacts
(
	 CUST_ID CHAR(50)
	,CONTACT_DATE DATE
	,CONTACT_TYPE CHAR(1)
)

CREATE TABLE lines
(
	 CUST_ID CHAR(50)
	,ORDERNUM char(50)
	,ORDER_DATE DATE
	,LINEDOLLARS FLOAT
	,GIFT CHAR(1)
	,RECIPNUM CHAR(50)
)

CREATE TABLE orders
(
	 CUST_ID CHAR(50)
	,ORDERNUM CHAR(50)
	,ORDER_DATE DATE
	,ORDERMETHOD CHAR(5)
	,PAYMENTTYPE CHAR(5)
)